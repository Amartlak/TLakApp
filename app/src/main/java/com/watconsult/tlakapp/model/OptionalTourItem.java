    public void setPromoContent(String promoContent) {
        this.promoContent = promoContent;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getOptionalDepartureImage() {
        return optionalDepartureImage;
    }

    public void setOptionalDepartureImage(String optionalDepartureImage) {
        this.optionalDepartureImage = optionalDepartureImage;
    }
